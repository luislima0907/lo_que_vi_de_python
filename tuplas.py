#Creando una tupla con la palabra clave o funcion (tuple), debemos de darle una lista normal dentro de los parametros para que esta tupla se pueda crear
tupla = tuple(["Jajaja", "XDDD", 10])
print(tupla)

#Tambi√©n podemos crear tuplas separando los datos o elementos que contenga una variable por una coma.
tupla_dos = "Nombre", "Apellido"
print(tupla_dos)

#Y si queremos crear una tupla con un solo elemento, debemos de escribir una coma al final del dato que le asignamos a la variable.
tupla_con_un_dato = "Hola",
print(tupla_con_un_dato)

#Esto es util para leer datos fijos y que no se puedan modificar, que es lo contrario a lo que hace una lista, que es almacenar un dato para su posible modifcacion y utiliza dos espacios de la memoria para hacer ese proceso, en cambio con la tupla eso no se puede hacer

